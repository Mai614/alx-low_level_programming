#ifndef DOG_H
#define DOG_H
/**
 * struct dog - a structure representing a dog
 * @name: pointer to the name of the dog
 * @age: age of the dog
 * @owner: pointer to the owner's name of the dog
 *
 * Description: This structure represents a dog and its attributes.
 */

struct dog
{
char *name;
float age;
char *owner;
};

#endif /*DOG_H*/
